package midweek7;
//Write an infinite loop.
//A infinite loop never ends. Condition is always true.

public class Infinite_Loop {
    public static void main(String[] args) {  //main method
        int i =100; // declared variale
        while (i>=-100){ // while loop stating 100 is greater than -100
            System.out.println("infinite"); //print statment
            i++; //increament
        }



    }
}